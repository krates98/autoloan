router.get("/getip", async function (req, res) {
  const data = jsonData.data;

  if (Array.isArray(data) && data.length > 0) {
    // Check if the data is a valid array
    // Select a random IP address from the list
    const randomIndex = Math.floor(Math.random() * data.length);

    const ip = data[randomIndex].ip;
    const port = data[randomIndex].port;
    const ipPort = ip + ":" + port;
    // Render the EJS template with the IP address
    res.render("getip", { ipPort });
  } else {
    // Render a custom error page with the error message
    res.render("error", { message: "Failed to fetch IP address data" });
  }
});

